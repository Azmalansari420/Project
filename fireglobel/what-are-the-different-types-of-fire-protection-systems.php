							<p class="jsticnt">It has become more obvious over the years how important it is to have a proper fire
								protection system in each and every building. For most of the people, what comes to their mind when they
								think of a fire protection system are smoke detectors or sprinklers and fire extinguishers. However,
								these three elements cover only a very small portion of the fire protection services you should have at
								all times. There are various parts of a building’s fire protection that are often ignored or are
								completely forgotten. </p>

							<h2 class="text-primary srvh2">The Two Different Type Of Fire Protection System Are</h2>

							<p class="jsticnt">There are in fact two types of fire protection system: Active Fire Protection (AFP) and
								Passive Fire Protection (PFP). One type of protection should never be chosen over the other because both
								together are important. both of the methods AFP and PFP must be used together for full fire protection
								of your building. It is important for all to understand the difference between AFP and PFP so that you
								get confident that your building is protected by both types of fire protection services. </p>

							<h2 class="text-primary srvh2">Active Fire Protection System:</h2>
							<p class="jsticnt">The Active Fire Protection system consists of various components of fire protection
								that requires some kind of action to work. These actions may be manual, like using a fire extinguisher
								or using a fire hose and hydrant, or automatic like the fire sprinkler system and smoke detector. The
								action that results from the active fire protection system is always triggered by some sort of alert or
								signal from within the system. The action itself will help to contain, suppress and try to completely
								eliminate a fire that has already started. Although fire suppression and extinguishing systems are the
								most obvious examples of AFP, fire detection and fire alarm systems are equally as important and are
								also considered under AFP. After picking up a signal like smoke or heat, these systems will trigger a
								response such as alerting the fire department and activating sprinklers. Working fire alarm and
								detection systems and fire suppression systems increases your chances of suppressing and extinguishing a
								fire before it causes harm to property and human life. </p>

							<h2 class="text-primary srvh2">Passive Fire Protection</h2>
							<p class="jsticnt">Passive Fire protection is mostly overlooked but is a very important fundamental
								component of your fire protection system. Passive Fire Protection is a set of components which is used
								to compartmentalize a building in order to keep a fire from spreading and require no action to work.
								Passive fire protection is usually structural and built into the building while constructing it. By
								building fire-resistant walls and floors, Passive Fire Protection gives people time to escape from a
								building that has a fire emergency. Other examples of Passive Fire Protection include dampers which
								prevent the spread of fire and smoke through a building’s ductwork, and fire doors which
								compartmentalize and stop fire from spreading even more. Fire-proofing successfully compartmentalizes
								fires and keeps damage to a minimum area by eliminating possible fuel a fire could use to spread or
								ignite with in the first place. </p>

							<h2 class="text-primary srvh2">Choosing The Right Fire Protection System For You</h2>
							<p class="jsticnt">There should be no argument over which type of protection is better as both are
								important Active Fire Protection and Passive Fire Protection systems perform fundamentally different
								tasks that are equally important. Active Fire protection takes action in order to extinguish and
								suppress fire. Passive Fire Protection will help to prevent and contain a fire from spreading to other
								parts of the building. They work together by alerting people inside the building of a fire and safely
								containing the fire so that people may evacuate and/or try to suppress the fire. Global Fire & Safety
								services provide active fire protection services like installation of fire suppression systems and fire
								safety products such as extinguishers sprinklers etc. </p>


