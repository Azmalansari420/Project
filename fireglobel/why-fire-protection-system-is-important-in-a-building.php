							<p class="jsticnt">A fire protection system is one of the most important safety components of a building,
								regardless of whatever type of building it is residential complex, commercial facility, hospital or
								educational facility. Without any fire protection system, the precious lives of those who are living or
								working inside the building are placed at a high risk in the event of any fire emergency. That’s why the
								fire protection systems have been designed to help protect the building and its occupants during a fire.
								Buildings are enclosed in shape and don’t have much space for protection if any fire broke out there
								without a proper fire protection system it’ll be very dangerous for people inside it. </p>


							<h2 class="text-primary srvh2">There Are Two Type Of Fire Protection System</h2>
							<p class="jsticnt">The Passive Fire Protection system uses systems that don’t require any motion or action
								in order to work related to eliminating fire. Smoke and fire Dampers prevent the spreading of flames and
								smoke in the building through the ductwork of a facility. Fire Walls and Barriers or even the Fire
								Doors, if kept shut, help to contain the fire only to one specific location of the building and stops it
								from doing further damage to other parts of the building. Photo luminescent and glowing Path Marking
								Systems and Exit Signs will continue to glow and show way in smoky conditions, allowing people to
								navigate their way to safety. Ultimately passive fire protection systems compartmentalize a building
								into sections so that in a fire emergency it will contain and slow the spread of the fire and smoke,
								while helping to guide occupants safely out of danger. Passive fire protection system is a safety
								measure which is made when the construction of a building is in process, but to ensure completely that
								the building and everyone living or working inside will be completely protected, the building needs to
								have both passive and active fire protection systems.</p>


							<p class="jsticnt">Active Fire Protection system uses a system that requires a certain amount of motion
								and action in order to work properly and accurately. These systems use some amount of actions and
								reactions to notify us of the fire and smoke conditions, Active fire protection system comprises fire
								sprinkler, Smoke detector, Smoke alarm, fire extinguishers, fire hydrants and manual fire fighting.
								Active fire protection works on collective working of all the above given fire protection components.
								But the actual fire protection is done by an active fire protection system as it eliminates and controls
								fire actually instead of containing it to one part of the building. </p>


							<h2 class="text-primary srvh2">Active and Passive Both Important For Building</h2>
							<p class="jsticnt">As you can see, a building’s fire protection system plays a very important role in
								providing the building and people working and living in it with protection in the event of any fire
								emergency. Each system approaches towards the threat differently, but is critical to the integrity of
								the building and the safety of those inside. However, in order for these systems to work properly they
								need to be maintained and kept up-to-date with the latest fire safety code. </p>


