							<h1 class="text-primary my-4 srvh1">Why Fire Protection System Is Important In A Building?</h1>
							<div class="meta mb-4">

								<div class="meta-date d-inline"><i class="fa fa-clock-o"></i> May 16, 2020</div>
								<div class="meta-author d-inline">Posted by Global Fire</div>
