							<p class="jsticnt">We tend to think of fire extinguishers as saviors as they’re always nearby, ready and
								waiting in case a fire emergency breaks out. But the truth is that fire extinguishers often are not
								ready, because they haven’t been properly kept and maintained. There are countless stories of people
								attempting to stop an raging fire with a portable extinguisher and then they realize that it’s empty or
								not being maintained properly and it is not in any working condition. </p>


							<p class="jsticnt">Everyone from homeowners to business owners needs to understand the importance of
								timely recharging and maintenance of fire extinguishers. This process is a very important aspect as
								service, maintenance and refilling cannot be ignored as they play a very important role in fighting any
								fire emergency.</p>

							<h2 class="text-primary srvh2">When And Where To Refill Fire Extinguisher?</h2>
							<ul class="list">

								<li>
									<p class="jsticnt">Fire extinguishers need to be recharged immediately after every use as the material
										inside them is not permanent. Even if the extinguishing agent inside the extinguisher is not
										completely discharged, still the extinguisher needs to be serviced and maintained in order to make
										sure it’s ready for its next use. Even a minimal discharge or usage from extinguisher will normally
										result in leakage and an eventual total loss of pressure needed for proper operation for fire
										emergencies.</p>
								</li>

								<li>
									<p class="jsticnt">Fire extinguishers need to be recharged in certain period of time throughout their
										life, even if they haven’t been used. Fire extinguishers require periodic internal examination,
										maintenance and recharging. So that they works smoothly at the time of fire emergency.</p>
								</li>

								<li>
									<p class="jsticnt">Extinguishers also need to be internally examined in certain period of time or
										recharged due to damage or other environmental factors that may result in extinguishers becoming
										depressurized.</p>
								</li>

							</ul>

							<h2 class="text-primary srvh2">What is the fire extinguisher recharging process?</h2>
							<p class="jsticnt">Fire extinguisher recharging or refilling should be performed by trained professionals.
								Fire protection services companies have the proper tools, training, knowledge and workers to ensure that
								your fire extinguishers are in perfect working condition and that they have been properly refilled with
								the material which is meant to be filled inside. </p>

							<p class="jsticnt">This is not a guide for how to recharge a fire extinguisher, but just an overview so
								that you can understand the process. </p>

							<ul class="list">

								<li>
									<p class="jsticnt">First, the fire extinguisher is completely depressurized and the agent removed from
										the cylinder.</p>
								</li>

								<li>
									<p class="jsticnt">The discharge valve is removed from the fire extinguisher.</p>
								</li>

								<li>
									<p class="jsticnt">Then the extinguisher is refilled with extinguishing agent (water, carbon dioxide,
										dry chemical, etc.) according to the weight and size of the cylinder </p>
								</li>

								<li>
									<p class="jsticnt">Then The extinguisher is re-pressurized with the appropriate pressurizing gas .</p>
								</li>

								<li>
									<p class="jsticnt">The technicians complete a final Leak Test of the extinguisher and reinstall the
										discharge nozzle or the hose.</p>
								</li>

								<li>
									<p class="jsticnt">The extinguisher is weighed again to confirm that the total weight and A brand new
										tamper seal is installed on the safety pin of the extinguisher and a new recharge tag is attached to
										the extinguisher having details of weight and completion of process.</p>
								</li>

							</ul>

